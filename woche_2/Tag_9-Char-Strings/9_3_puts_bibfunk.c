// Beispiel für die Ausgabe von Strings mit puts()

#include<stdio.h>

int main(int argc, char *argv[]){

    char *m1 = "C ist";
    char *m2 = "die";
    char *m3 = "beste";
    char *m4 = "Programmiersprache";
    char *m5 = "der Welt";

    puts(m1);
    puts(m2);
    puts(m3);
    puts(m4);
    puts(m5);

    return 0;
}